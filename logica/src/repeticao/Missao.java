package repeticao;

import javax.swing.JOptionPane;

public class Missao {

	public static void main(String[] args) {

		int numero = Integer.parseInt(JOptionPane.showInputDialog("Número"));
		int chute = 0;
		int contador = 0;
		do {
			chute = Integer.parseInt(JOptionPane.showInputDialog("Adivinhe o número"));
			contador=contador+1; 
			if (chute>numero) {
				JOptionPane.showMessageDialog(null, "Você está chutando alto");
				//System.out.println("Você está chutando alto");
			}else if (chute<numero) {
				JOptionPane.showMessageDialog(null, "Você está chutando baixo");
				//System.out.println("Você está chutando baixo");
			}
		}while(numero!=chute);
			JOptionPane.showMessageDialog(null, "Parabéns você acertou com " + contador + " tentativas(s)");
			//System.out.println("Parabéns você acertou");
		
		
		
	}
}
